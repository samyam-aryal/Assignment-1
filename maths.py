def add(a, b):
    '''Take two numbers as argument, add them and return the sum'''
    return a+b

def subtract(a, b):
    '''Take two numbers as argument, subtract the second number from the first and return the sum'''
    return a-b


def multiply(a, b):
    '''Take two numbers as argument and return their product'''
    return a*b

def divide(a, b):
    '''Takes two arguments a & b, and returns a/b'''
    return a/b

